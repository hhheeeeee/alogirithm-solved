def find(node):
    while root[node] != node:
        node = root[node]
    return root[node]


def union(a, b):
    a = find(a)
    b = find(b)
    root[b] = a


N, Q = map(int, input().split())
log = []
root = [i for i in range(N+1)]
for order in range(1, N + 1):
    x1, x2, y = map(int, input().split())
    log.append((x1, x2, y, order))

log = sorted(log)

nowstart, nowend, nowindex = log[0][0], log[0][1], log[0][3]

for i in range(1, N):
    nextstart, nextend, nextindex = log[i][0], log[i][1], log[i][3]
    if nowstart <= nextstart <= nowend:
        union(nowindex, nextindex)
        if nextend >= nowend:
            nowstart, nowend, nowindex = nextstart, nextend, nextindex
    else:
        nowstart, nowend, nowindex = nextstart, nextend, nextindex

# print(root)
